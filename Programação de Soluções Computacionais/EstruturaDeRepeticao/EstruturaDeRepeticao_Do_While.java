public class EstruturaDeRepeticao_Do_While {
    
        public static void main(String[] args) {
            int numero = 10;
           
            do{
                //faça
                //tomar cuidado, pq ele faz primeiro e depois verifica a condição;
                System.out.println(numero);
                //imprime o número
                numero++;
                //incremento, acrescenta 1 ao 10;
                //numero++ --> numero + 1;
                //numero+=2 --> numero + 2;
            }while(numero<20);
            //estrutura de repetição(contador), vai seguir enquanto o valor for menor que 20. Só vai para quando a condição não for mais verdadeira;
           
        }
       
    }

